print("secuencia del 0 al 4: ")
for i in range(5):
    print(i, end=" ")

print("\nsecuencia del 10 al 20: ")
for i in range(10, 20 +1): # el +1 es para que se incluya el 20 en el rango
    print(i, end=" ")

print("\nsecuencia del 20 al 30 de 2 en 2: ")
for i in range(20, 30 + 1,2):
    print(i, end=" ")
